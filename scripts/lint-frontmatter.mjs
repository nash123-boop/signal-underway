import fs from "node:fs";
import path from "node:path";
import { isPublicationTitleCase } from "./publication-style.mjs";

const directory = path.resolve("src/content/posts");
const files = fs.readdirSync(directory).filter((file) => file.endsWith(".md"));
const required = ["title", "deck", "category", "publishDate"];
let valid = true;

for (const file of files) {
  const raw = fs.readFileSync(path.join(directory, file), "utf8").replace(/^\uFEFF/, "");
  const match = raw.match(/^---\r?\n([\s\S]*?)\r?\n---\r?\n([\s\S]*)$/);
  if (!match) {
    console.error(`ERROR ${file}: malformed or missing frontmatter`);
    valid = false;
    continue;
  }
  const frontmatter = match[1];
  const body = match[2].trim();
  const title = frontmatter.match(/^title:\s*["']?(.*?)["']?\s*$/m)?.[1] ?? "";
  for (const field of required) {
    if (!new RegExp(`^${field}:`, "m").test(frontmatter)) {
      console.error(`ERROR ${file}: missing ${field}`);
      valid = false;
    }
  }
  if (body.split(/\s+/).filter(Boolean).length < 80) {
    console.error(`ERROR ${file}: article body must contain at least 80 words`);
    valid = false;
  }
  if (/^#\s+/m.test(body)) {
    console.error(`ERROR ${file}: do not add an H1 in article content; the title is generated by the article template`);
    valid = false;
  }
  if (!isPublicationTitleCase(title)) {
    console.error(`ERROR ${file}: article title must use publication title case`);
    valid = false;
  }
  for (const headingMatch of body.matchAll(/^(#{2,6})\s+(.+)$/gm)) {
    const [, marks, heading] = headingMatch;
    if (marks.length > 3) {
      console.error(`ERROR ${file}: use only H2 (##) and H3 (###) headings`);
      valid = false;
    }
    if (!isPublicationTitleCase(heading.replace(/[*_`]/g, ""))) {
      console.error(`ERROR ${file}: heading "${heading}" must use publication title case`);
      valid = false;
    }
  }
  if (/style\s*=/i.test(body)) {
    console.error(`ERROR ${file}: inline styling is not allowed in article content`);
    valid = false;
  }
  if (/yourdomain|\.\.\.|TBD|TODO/i.test(raw)) {
    console.error(`ERROR ${file}: contains placeholder content`);
    valid = false;
  }
}

if (!valid) process.exit(1);
console.log(`Validated ${files.length} articles.`);
